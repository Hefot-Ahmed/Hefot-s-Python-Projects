def add(a, b):
  answer = a + b
  return answer

def subtract(a, b):
  answer = a - b
  return answer

def multiply(a, b):
  answer = a * b
  return answer

def divide (a, b):
  answer = a / b
  return answer

def exp(a, b):
  answer = a ** b
  return answer


print(add(2, 3))
print(subtract(5, 2))
print(multiply(4, 3))
print(divide(10, 2))
print(exp(2, 3))
